def run(airdrop_id: str):
    print(f'Airdrop #{airdrop_id} in progress!')

    print(f'Total {len(addresses)} addresses!')

    opted_in = filter_opted_in(airdrop_id, addresses)
    print(f'Total {len(opted_in)} addresses are opted-in!')
    print(opted_in)

    tokens_per_addr = AIRDROP_SUPPLY / len(opted_in)
    print(f'Total {AIRDROP_SUPPLY} tokens, {tokens_per_addr} META per address!\n')

    amount_sent = 0
    addrs_sent = 0

    for address in opted_in:
        try:
            amount = send_tokens(address, tokens_per_addr, airdrop_id)
            amount_sent += amount
            addrs_sent += 1
            print(f'Sent {amount} to {address}')
        except Exception as e:
            print(e, '\n', address)

    print(f'Sent {amount_sent} tokens to {addrs_sent} holders!')
